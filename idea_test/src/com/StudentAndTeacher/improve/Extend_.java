package com.StudentAndTeacher.improve;

public class Extend_ {
    private String name;
    private int age;
    public Extend_(){}
    public Extend_(String name,int age){
        this.name=name;
        this.age=age;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
